function getCurrentSeason() {
    // It's plus one because January is index 0
    const now = new Date();
    const month = now.getMonth() + 1;

    if (month > 3 && month < 6) {
        return  'üå±';
    }

    if (month > 6 && month < 9) {
        return 'üåû';
    }

    if (month > 9 && month < 12) {
        return 'üçÅ';
    }

    if (month >= 1 && month < 3) {
        return '‚òÉÔ∏è';
    }
}

